def get_token():
    url = f"{BASE_URL}/oauth2/tokenP"
    body = {"grant_type": "client_credentials", "appkey": APP_KEY, "appsecret": APP_SECRET}
    res = requests.post(url, json=body, headers={"content-type": "application/json"})
    if res.status_code == 200:
        return res.json().get("access_token")
    print(f"❌ 토큰 발급 실패: {res.status_code} {res.text}")
    return None


def get_price(token, code):
    """종목 현재가 1건 조회 (REST)"""
    url = f"{BASE_URL}/uapi/domestic-stock/v1/quotations/inquire-price"
    headers = {
        "authorization": f"Bearer {token}",
        "appkey": APP_KEY,
        "appsecret": APP_SECRET,
        "tr_id": "FHKST01010100",
        "content-type": "application/json; charset=utf-8",
    }
    params = {"FID_COND_MRKT_DIV_CODE": "J", "FID_INPUT_ISCD": code}

    try:
        res = requests.get(url, headers=headers, params=params, timeout=5)
        if res.status_code != 200:
            return None
        data = res.json()
        if data.get("rt_cd") != "0":
            return None
        o = data["output"]
        
        # 종목명 추출 (우선순위: hts_kor_isnm -> bstp_kor_isnm -> rprs_mrkt_kor_name)
        # 만약 rprs_mrkt_kor_name이 'KOSPI 200' 같은 시장 명칭이면 제외하도록 로직 추가
        name = o.get("hts_kor_isnm", "").strip()
        if not name or name == "KOSPI 200" or name == "코스피 200":
            name = o.get("bstp_kor_isnm", "").strip()
        if not name:
            name = o.get("rprs_mrkt_kor_name", "").strip()
            
        cap_억 = int(o.get("hts_avls", 0))
        if cap_억 == 0:
            return None
            
        return {
            "name": name,
            "ticker": code,
            "price": int(o.get("stck_prpr", 0)),
            "change": float(o.get("prdy_ctrt", 0)),
            "cap": cap_억,  # 억 단위
            "volume": int(o.get("acml_vol", 0)),
        }
    except Exception as e:
        return None


