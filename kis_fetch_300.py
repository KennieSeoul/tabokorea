# Build WICS Heatmap JSON
# ═══════════════════════════════════════════
def build_heatmap_json(stocks):
    """
    종목 리스트를 WICS 3단계(대분류→중분류→종목) 히트맵 JSON으로 변환
    (소분류는 종목 메타데이터로 보존)
    """
    # 대분류 → 중분류 → [종목]
    tree = {}
    for s in stocks:
        wics = WICS_MAP.get(s["ticker"])
        if wics:
            sec, mid, ind = wics
        else:
            sec, mid, ind = "기타", "기타", "기타"

        if sec not in tree:
            tree[sec] = {}
        if mid not in tree[sec]:
            tree[sec][mid] = []

        tree[sec][mid].append({
            "name": s["name"],
            "ticker": s["ticker"],
            "cap": s["cap"],        # 억 단위
            "change": s["change"],
            "industry": ind,
        })

    # D3 treemap 형식으로 변환
    result = {
        "name": "KRX",
        "updated": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "children": [],
    }

    for sec_name, mid_groups in tree.items():
        sector = {"name": sec_name, "children": []}
        for mid_name, stock_list in mid_groups.items():
            mid = {
                "name": mid_name,
                "children": sorted(stock_list, key=lambda x: x["cap"], reverse=True),
            }
            sector["children"].append(mid)
        # 중분류를 시총 합계 순으로 정렬
        sector["children"].sort(
            key=lambda m: sum(s["cap"] for s in m["children"]), reverse=True
        )
        result["children"].append(sector)

    # 대분류를 시총 합계 순으로 정렬
    result["children"].sort(
        key=lambda sec: sum(
            sum(s["cap"] for s in m["children"])
            for m in sec["children"]
        ),
        reverse=True,
    )

    return result


