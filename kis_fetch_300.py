def main():
    print("=" * 60)
    print(f"  KRX 시총 상위 종목 조회 + WICS 히트맵 JSON 생성")
    print(f"  {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 60)
    print()

    if APP_KEY == "YOUR_APP_KEY":
        print("⚠️  APP_KEY를 실제 값으로 변경해주세요!")
        return

    # 1. 토큰 발급
    print("🔑 토큰 발급 중...")
    token = get_token()
    if not token:
        return
    print("✅ 토큰 발급 완료")
    print()

    # 2. 종목 조회 (초당 20건 제한 → 18건으로 여유 있게)
    codes = TOP_STOCK_CODES
    total = len(codes)
    print(f"📊 {total}개 종목 조회 시작 (초당 ~18건, 예상 {total // 18 + 1}초)")
    print("-" * 60)

    stocks = []
    errors = 0
    batch_count = 0

    for i, code in enumerate(codes):
        result = get_price(token, code)
        if result:
            stocks.append(result)
            if (i + 1) % 50 == 0:
                print(f"  ... {i + 1}/{total} 완료 ({len(stocks)}개 성공)")
        else:
            errors += 1

        batch_count += 1
        # 초당 18건 제한
        if batch_count >= 18:
            time.sleep(1.05)
            batch_count = 0

    print(f"\n✅ 조회 완료: {len(stocks)}개 성공, {errors}개 실패")
    print()

    # 시총 기준 상위 300개만
    stocks.sort(key=lambda x: x["cap"], reverse=True)
    stocks = stocks[:300]

    # 3. 통계
    up = sum(1 for s in stocks if s["change"] > 0.3)
    down = sum(1 for s in stocks if s["change"] < -0.3)
    flat = len(stocks) - up - down
    total_cap = sum(s["cap"] for s in stocks)

    print(f"📈 상승: {up}개  📉 하락: {down}개  ➡️ 보합: {flat}개")
    print(f"💰 총 시가총액: {total_cap:,.0f}억원")
    print()

    # 상위 10 출력
    print("🏆 시총 상위 10:")
    for i, s in enumerate(stocks[:10]):
        arrow = "▲" if s["change"] > 0 else "▼" if s["change"] < 0 else "−"
        sign = "+" if s["change"] > 0 else ""
        wics = WICS_MAP.get(s["ticker"], ("?", "?", "?"))
        print(f"  {i+1:2d}. {s['name']:14s}  {s['price']:>10,}원  "
              f"{arrow}{sign}{s['change']:.2f}%  "
              f"시총 {s['cap']:>10,}억  [{wics[0]}>{wics[1]}]")
    print()

    # 4. 히트맵 JSON 생성
    heatmap = build_heatmap_json(stocks)

    output_file = "public/krx_heatmap_data.json"
    with open(output_file, "w", encoding="utf-8") as f:
        json.dump(heatmap, f, ensure_ascii=False, indent=2)

    print(f"💾 히트맵 JSON 저장: {output_file}")
    print(f"   대분류 {len(heatmap['children'])}개 섹터")

    for sec in heatmap["children"]:
        mid_count = len(sec["children"])
        stock_count = sum(len(m["children"]) for m in sec["children"])
        print(f"   ├── {sec['name']}: {mid_count}개 중분류, {stock_count}개 종목")

    print()
    print("✅ 완료! 이 JSON 파일을 히트맵에 연결하면 실데이터로 동작합니다.")
    print()
    print("📌 다음 단계:")
    print("   1. krx_heatmap_data.json을 히트맵 컴포넌트에서 fetch")
    print("   2. 3분마다 자동 갱신하는 cron/interval 설정")
    print("   3. Firebase에 배포")


if __name__ == "__main__":
    main()